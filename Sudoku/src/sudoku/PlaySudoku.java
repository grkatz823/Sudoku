    /*
     * Calls the Sudoku Solver function and then fills in all the fields that
     * can be changed
     */
    private void SolveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SolveButtonActionPerformed
        // TODO add your handling code here:
        SudokuSolver solv = new SudokuSolver();
        textFields = addJTextFields();
        sudoku = solv.SudokuSolver(origSudoku);
        for (int i = 0; i < sudoku.slots.length; i++) {
            if (textFields.get(i).isEditable()) {
                textFields.get(i).setText(Integer.toString(sudoku.slots[i]));
            }
        }
    }//GEN-LAST:event_SolveButtonActionPerformed

    /*
     * Saves the gameboard, in its original and current forms, in a text file
     */
    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        try {
            String newFile = " ";
            textFields = addJTextFields();

            //Saves the original setup of the sudoku
            for (int i = 0; i < origSudoku.slots.length; i++) {
                newFile += Integer.toString(origSudoku.slots[i]);
            }

            newFile += "\n";

            //Saves the current board
            for (int i = 0; i < textFields.size(); i++) {
                String textValue = textFields.get(i).getText();
                if (textValue.equals("")) {
                    newFile += 0;
                } else {
                    newFile += textValue;
                }
            }


            File outFile = new File(jTextField82.getText());
            outFile.delete();
            try (FileWriter out = new FileWriter(outFile)) {
                out.write(newFile);
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_SaveButtonActionPerformed

    /*
     * Takes the filepath chosen in browse, adds a .doc if necessary and places
     * it in the corresponding text-field
     */
    private void SaveBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBrowseActionPerformed
        // TODO add your handling code here:
        try {
            fc = new JFileChooser();

            int returnValue = fc.showOpenDialog(PlaySudoku.this);

            if (returnValue == JFileChooser.APPROVE_OPTION) {
                String input = fc.getSelectedFile().toString();
                if (input.endsWith(".doc")) {
                    jTextField82.setText(input);
                } else {
                    jTextField82.setText(input + ".doc");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "The filepath you chose does not exist,please choose another.");
            System.err.println(e);
        }
    }//GEN-LAST:event_SaveBrowseActionPerformed

