    /*
     * Loads the gameboard
     */
    private void LoadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoadButtonActionPerformed
        // TODO add your handling code here:
        try {
            
            origSudoku = new SudokuCreator();
            //clears current gameboard
            textFields = addJTextFields();
            for (int i = 0; i < textFields.size(); i++) {
                textFields.get(i).setEditable(true);
                textFields.get(i).setText("");
            }

            //Scans the file's contens in and takes the first line
            File file = new File(jTextField83.getText());
            Scanner scan = new Scanner(file);

            String currentLine = scan.nextLine();
            String fullFileString = currentLine;
            //System.out.println(fullFileString);
            int currSlot;


            /*
             * If the slot was originally filled on the gameboard. We make that
             * slot non-editable and fill it with its corresponding value
             */
            for (int i = 1; i < fullFileString.length(); i++) {
                currSlot = Integer.parseInt(fullFileString.substring(i, i + 1));
                if (currSlot != 0) {
                    origSudoku.slots[i-1] = currSlot;
                    textFields.get(i-1).setText(fullFileString.substring(i, i + 1));
                    textFields.get(i-1).setEditable(false);
                }
            }

            /*
             * Otherwise, the slot was manually filled so it is assigned the
             * value
             */
            currentLine = scan.nextLine();
            fullFileString = currentLine.trim();
            //System.out.println(fullFileString);
            for (int i = 0; i < fullFileString.length(); i++) {
                currSlot = Integer.parseInt(fullFileString.substring(i, i + 1));
                if (currSlot != 0 && textFields.get(i).isEditable()) {
                    textFields.get(i).setText(fullFileString.substring(i, i + 1));
                }
                sudoku.slots[i] = currSlot;
            }


        } catch (FileNotFoundException | NumberFormatException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_LoadButtonActionPerformed

